def test_set_get_field(double_mean_game, field):
    double_mean_game.field = field
    assert double_mean_game.field.ndim == 3
    assert (double_mean_game.field == field).all()


def test_evolve(double_mean_game, field, evolved_field):
    double_mean_game.field = field
    double_mean_game.evolve(10)
    print(np.square(double_mean_game.field - evolved_field).sum())
    assert np.square(double_mean_game.field - evolved_field).sum() == 0


def test_densities(double_mean_game, field):
    double_mean_game.field = field
    double_mean_game.evolve(10)
    np.testing.assert_allclose(double_mean_game.densities,
                                np.array([[0.8775, 0.3425, 0.39 , 0.4725,
                                           0.5225, 0.675 , 0.64 , 0.6175,
                                           0.6825, 0.6425, 0.655],
                                          [0.8775, 0.3425, 0.39 , 0.4725,
                                           0.5225, 0.675 , 0.64 , 0.6175,
                                           0.6825, 0.6425, 0.655]]))

